  /**********************************************************************
* 函数名称：LCD_write_byte()
* 函数功能：写一个字节
* 入口参数：data,要写入的字节;command:0为命令,1为数据
* 出口参数：无
* 修改人  ：BDC
* 修改时间：2011/08/04
***********************************************************************/
void LCD_write_byte( unsigned char data, unsigned char command)
{
   unsigned char i;    
  CLR_LCD_CE;    
  if (command == 0)     
   CLR_LCD_DC;	//0
  else    
   SET_LCD_DC;	//1
	for(i=0;i<8;i++)
	{
		if(data&0x80)
			SET_SDIN; //1
		else
			CLR_SDIN; //0
		CLR_SCLK;	  //0
		data= data<< 1;
		SET_SCLK;	 //1
	}   
   SET_LCD_CE;
}

   /**********************************************************************
* 函数名称：LCD_clear()		
* 函数功能：液晶清屏
* 入口参数：无
* 出口参数：无
* 修改人  ：BDC
* 修改时间：2011/08/04
**********************************************************************/
void LCD_clear(void)
  {
    unsigned int i;
    LCD_write_byte(0x0c, 0);			
    LCD_write_byte(0x80, 0);	
    for (i=0; i<504; i++)
      LCD_write_byte(0, 1);			
  }
 
/**********************************************************************
* 函数名称：LCD_Init()		
* 函数功能：初始化nokia5110
* 入口参数：无
* 出口参数：无
* 修改人  ：BDC
* 修改时间：2011/08/04
**********************************************************************/
void LCD_Init(void)
  {           
	LCD_IO_init();

    CLR_LCD_RST; // 产生一个让LCD复位的低电平脉冲
    delay_us_5110(1);
    SET_LCD_RST;
    CLR_LCD_CE;// 关闭LCD  
    delay_us_5110(1);		
    SET_LCD_CE;// 使能LCD
    delay_us_5110(1);
    LCD_write_byte(0x21, 0);	// 使用扩展命令设置LCD模式
    LCD_write_byte(0xbf, 0);	// 设置偏置电压	 b6
    LCD_write_byte(0x06, 0);	// 温度校正
    LCD_write_byte(0x13, 0);	// 1:48
    LCD_write_byte(0x20, 0);	// 使用基本命令
    LCD_clear();	            // 清屏
    LCD_write_byte(0x0c, 0);	// 设定显示模式，正常显示        
    CLR_LCD_CE;               // 关闭LCD  
  }

